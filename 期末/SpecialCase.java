public class SpecialCase {
	public static String getName (int number) {
		String name = "";
		if (number == 1) {
			name = "a";
		} else if (number == 2) {
			name = "b";
		} else if (number == 4) {
			name = "c";
		} else if (number == 5) {
			name = "d";
		} else if (number == 6) {
			name = "e";
		} else if (number == 9) {
			name = "f";
		}
		return name;
	}
}